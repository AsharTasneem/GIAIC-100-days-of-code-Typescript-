var places = ["New Zealand", "USA", "Belgium", "Zimbabwe", "Australia", "Russia",];
console.log("Original Order: ".concat(places));
var alphabaticOrder = places.sort();
console.log("Alphabetic Order: ".concat(alphabaticOrder, "."));
var reverseAlphabeticOrder = places.sort().reverse();
console.log("Reverse Order: ".concat(reverseAlphabeticOrder, "."));
